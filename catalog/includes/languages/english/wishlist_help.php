<b>How do I add items to My Wish List? </b><br>
To add an item to My Wish List, just click the "Add to Wishlist" button for any item that interests you. The "Add to Wishlist" button appears next to the "Add to Cart" button in the product info pages.
<br>
<br>
<b>Can I add "out-of-stock" items or "coming-soon items"?</b><br>
Yes. You can add any item you choose to My Wish List.
<br>
<br>
<b>How do I view My Wish List?</b><br>
On the right column is a "My Wish List" box.  This box shows the current status of My Wish List.  If there are 5 or less items in My Wish List, they will be listed here.  If there are more than 5 items, there will be a simple counter with the total number of items in My Wish List.
<br>
<br>
My Wish List may be viewed at any time by clicking either the link found in the "My Wish List" box (<i><u>View My Wish List</u> [+]</i>).  or by clicking the right arrow on the "My Wish List" box.
<br>
<br>
<b>How do I move My Wish List items to my Shopping Cart?</b><br>
To move My Wish List items to the Shopping Cart, either click "Move to Cart" under the product name in the "My Wish List" box (if the My Wish List items are shown in the box) or click on "Move to Cart" underneath the item on the My Wish List main page and voila! The item is moved to the Cart.
<br>
<br>
<b>How do I remove items from My Wish List?</b><br>
To remove items from My Wish List, either click "Delete" under the item\'s name in the "My Wish List" box or click "Delete" underneath the item on the My Wish List main page of the item(s) you would like to remove. 
<br>
<br>
<b> Can I make My Wish List available to others? </b><br>
Sorry. Currently My Wish List is only accessible when you are logged in to your account. Therefore, you are the only one that can see it.  However, you can email your wishlist to a friend by visiting the My Wish List main page, entering their email address into the \'send your wishlist to a friend\' box and clicking the email envelope.
